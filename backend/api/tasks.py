router = APIRouter(prefix="/api")

async def get_current_user(request: Request):
    token = request.headers.get("Authorization")
    if not token:
        raise HTTPException(status_code=401, detail="Not authorized")
    return decode_token(token)

@router.post("/approve-plan")
async def approve_plan(request: Request, payload: dict = Depends(get_current_user)):
    data = await request.json()
    task_id = data.get("task_id")
    
    if not task_id:
        raise HTTPException(status_code=400, detail="Missing task_id")

    user_id = payload.get("id")

    conn = get_db_connection()
    from psycopg2.extras import RealDictCursor
    cursor = conn.cursor(cursor_factory=RealDictCursor)
    try:
        cursor.execute(
            "SELECT title, description, time, ai_plan_json FROM tasks WHERE id = %s AND user_id = %s",
            (task_id, user_id)
        )
        task = cursor.fetchone()
        if not task or not task['ai_plan_json']:
            raise HTTPException(status_code=404, detail="Task or plan not found")
        
        plan_data = task['ai_plan_json']
        if isinstance(plan_data, str):
            plan_data = json.loads(plan_data)
            
        # final_plan contains the stringified JSON from LangGraph
        final_plan_str = plan_data.get("final_plan", "")
        subtasks_list = plan_data.get("subtasks", []) if isinstance(plan_data, dict) else []

        try:
            if isinstance(final_plan_str, str):
                final_plan_obj = json.loads(final_plan_str)
            else:
                final_plan_obj = final_plan_str
            if isinstance(final_plan_obj, dict) and not subtasks_list:
                subtasks_list = final_plan_obj.get("subtasks", [])
        except Exception as e:
            print(f"Error parsing final_plan: {e}")
            subtasks_list = subtasks_list or []

        if not subtasks_list:
            # Fallback: create a single subtask so routines still show up
            subtasks_list = [{
                "subtask": task.get("title") or "Main Task",
                "description": task.get("description") or "Complete the task",
                "time_to": (task.get("time") or "00:00:00")
            }]

        for st in subtasks_list:
            # Use the time_to generated by AI, which should be incremental HH:MM
            time_to = st.get('time_to')
            if time_to and ":" in time_to:
                parts = time_to.split(':')
                if len(parts) == 2:
                    time_to = f"{parts[0].zfill(2)}:{parts[1].zfill(2)}:00"
            else:
                time_to = task['time'] # Fallback
                
            create_subtask(task_id, st.get('subtask'), st.get('description'), time_to)
        
        update_plan_approval_status(task_id, True)
        create_user_notification(user_id, f"Plan approved and subtasks scheduled for task: {task_id}")
        return {"status": "success", "message": "Plan approved and subtasks created"}
    except Exception as e:
        print(f"Approval Error: {e}")
        raise HTTPException(status_code=500, detail=str(e))
    finally:
        cursor.close()
        conn.close()

@router.post("/subtasks/{subtask_id}/toggle")
async def toggle_subtask(subtask_id: int, request: Request, payload: dict = Depends(get_current_user)):
    data = await request.json()
    is_completed = data.get("is_completed")
    
    from repos.task_repo import update_subtask_status, check_and_update_task_status
