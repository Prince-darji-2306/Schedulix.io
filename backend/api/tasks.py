@router.post("/tasks")
async def create_task(task_data: TaskCreate, payload: dict = Depends(get_current_user)):
    user_id = payload.get("id")
    
    # AI Plan Generation using LangGraph
    try:
        # Get current time for incremental scheduling with special rounding:
        # 0-19 -> 00, 20-40 -> 30, >40 -> next hour :00
        now = datetime.now()
        h, m = now.hour, now.minute
        if m < 20:
            m = 0
        elif 20 <= m <= 40:
            m = 30
        else:
            m = 0
            h = (h + 1) % 24
        now_time = f"{h:02d}:{m:02d}"
        
        ai_result = await ai_graph.ainvoke({
            "title": task_data.title, 
            "description": task_data.description or "",
            "current_time": now_time,
            "deadline": task_data.time, # The time part of the deadline
            "plans": [],
            "final_plan": ""
        })
            
        ai_plan_data = {"final_plan": ai_result["final_plan"]}
        ai_plan_json = json.dumps(ai_plan_data)
    except Exception as e:
        print(f"AI Generation Error: {e}")
        ai_plan_json = json.dumps({"error": "AI planning failed", "details": str(e)})

    # Use the provided time for task record creation
    success = create_task_record(
        task_data.title, 
        task_data.description, 
        user_id, 
        task_data.deadline_date, 
        task_data.time,
        task_data.status, 
        ai_plan_json
    )
    
    if success:
        return {"status": "success", "message": "Task and AI Plan created successfully"}
    raise HTTPException(status_code=500, detail="Failed to create task")

